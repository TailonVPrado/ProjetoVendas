    private void executaCosnultaGridInferior(){
        
        limpaItemVenda();
        
        String idVenda = (TableVenda.getValueAt(TableVenda.getSelectedRow(), 0)).toString();
        ArrayList<ItemVenda> listaItemVenda = new ArrayList<>();
        
        if(!(idVenda.equals(""))){
            ItemVendaService itemVendaService = new ItemVendaService();
            try {
                listaItemVenda  = itemVendaService.findByVenda(Integer.parseInt(idVenda));
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao realizar a consulta dos itens da compra ("+idVenda+"). Erro: " + ex.getMessage());
            }
        }
        
        Object[][] obj = new Object[listaItemVenda.size()][4];
        int linha = 0;
        
        for (ItemVenda itemVenda : listaItemVenda) {
            obj[linha][0] = itemVenda.getItem().getId();
            obj[linha][1] = itemVenda.getItem().getDescricao();
            obj[linha][2] = itemVenda.getQtde();
            String vlTotal = (itemVenda.getQtde() * itemVenda.getItem().getVlItem()) + "";
            if(vlTotal.contains(".")){
                vlTotal += "000";
                vlTotal = vlTotal.substring(0, vlTotal.indexOf(".") +3);
            }
            obj[linha][3] = vlTotal;
            linha++;
        }
        
        tableItemVenda.setModel(new javax.swing.table.DefaultTableModel(
            obj,
            new String [] {
                "Id Item", "Descrição", "Quantidade", "Valor"
            }
        ));
        
        TableColumn colId = tableItemVenda.getColumnModel().getColumn(0);
        TableColumn colDescricao = tableItemVenda.getColumnModel().getColumn(1);
        TableColumn colqtde = tableItemVenda.getColumnModel().getColumn(2);
        TableColumn colvalor = tableItemVenda.getColumnModel().getColumn(3);

        colId.setPreferredWidth(60);
        colDescricao.setPreferredWidth(225);
        colqtde.setPreferredWidth(70);
        colvalor.setPreferredWidth(100);

    }
    private void limpaItemVenda(){
        tableItemVenda.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {
                "Id Item", "Descrição", "Quantidade", "Valor"
            }
        ));
    }
