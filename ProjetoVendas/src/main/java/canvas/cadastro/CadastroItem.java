    private void idItemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idItemKeyReleased
        idItem.setText(idItem.getText().replaceAll("[^0-9]+", ""));
    }//GEN-LAST:event_idItemKeyReleased

    private void valorItemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorItemKeyReleased
        if(evt.getKeyCode() != 44)
            if(evt.getKeyCode() != 110)
                if(evt.getKeyCode() != 46)
                    valorItem.setText(valorItem.getText().replaceAll("[^0-9.]+", ""));
        if(valorItem.getText().contains(",")||valorItem.getText().contains(".")){
            String var = (valorItem.getText().replaceAll("[^,.]+", ""));
            if(var.length() > 1)
                valorItem.setText(valorItem.getText().substring(0, valorItem.getText().length()-1));
        }
        valorItem.setText(valorItem.getText().replaceAll("[,]+", "."));
    }//GEN-LAST:event_valorItemKeyReleased

    private void idItemFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_idItemFocusLost
        if(idItem.getText().equals("")){
            excluiItem.setEnabled(false);
            atualizaItem.setText("Cadastrar");
        }else{
            excluiItem.setEnabled(true);
            atualizaItem.setText("Atualizar");
            
            ItemService itemService = new ItemService();
            Item item = new Item();
            try {
                item = itemService.findById(Integer.parseInt(idItem.getText()));
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao buscar o item. Erro: "+ex.getMessage());
            }
            
            if(item.getId() == null){
                JOptionPane.showMessageDialog(null, "Item com id ("+idItem.getText()+") não encontrado.");
                excluiItem.setEnabled(false);
                atualizaItem.setText("Cadastrar");
                idItem.setText("");
            }else{
                descricaoItem.setText(item.getDescricao());
                valorItem.setText(item.getVlItem().toString());
            }
        }
    }//GEN-LAST:event_idItemFocusLost

    private void atualizaItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizaItemActionPerformed
        Item item = new Item();
        if(!idItem.getText().equals(""))
            item.setId(Integer.parseInt(idItem.getText()));
        item.setDescricao(descricaoItem.getText());
        if(!valorItem.getText().equals(""))
            item.setVlItem(Double.parseDouble(valorItem.getText()));
        
        ItemService itemService = new ItemService();
        if(idItem.getText().equals("")){
            try {   
                itemService.insert(item);
                idItem.setText(item.getId().toString());
                excluiItem.setEnabled(true);
                atualizaItem.setText("Atualizar");
                JOptionPane.showMessageDialog(null, "Item ("+item.getId()+") cadastrado com sucesso.");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar item. Erro: "+ex.getMessage());
            }
        }
        else{
            try {
                itemService.update(item);
                JOptionPane.showMessageDialog(null, "Cliente ("+item.getId()+") atualizado com sucesso.");
            } catch (DescricaoInvalidaException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao atualizar item. Erro: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_atualizaItemActionPerformed

