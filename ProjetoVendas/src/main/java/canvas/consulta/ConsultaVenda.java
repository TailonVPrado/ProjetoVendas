    private void valorInicialVendaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorInicialVendaKeyReleased
        if(evt.getKeyCode() != 44)
            if(evt.getKeyCode() != 110)
                if(evt.getKeyCode() != 46)
                    valorInicialVenda.setText(valorInicialVenda.getText().replaceAll("[^0-9.]+", ""));
        if(valorInicialVenda.getText().contains(",")||valorInicialVenda.getText().contains(".")){
            String var = (valorInicialVenda.getText().replaceAll("[^,.]+", ""));
            if(var.length() > 1)
                valorInicialVenda.setText(valorInicialVenda.getText().substring(0, valorInicialVenda.getText().length()-1));
        }
        valorInicialVenda.setText(valorInicialVenda.getText().replaceAll("[,]+", "."));
    }//GEN-LAST:event_valorInicialVendaKeyReleased

