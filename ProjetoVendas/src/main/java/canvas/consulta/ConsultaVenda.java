    private void consultarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarVendaActionPerformed
        limpaVenda();
        VendaService vendaService = new VendaService();
        ArrayList<Venda> listaVenda = new ArrayList<>();
        try {
            listaVenda = vendaService.findRel(montaWhere(), montaOrderBy());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao realizar a consulta. Erro: "+ ex.getMessage());
        }
        
        Object[][] obj = new Object[listaVenda.size()][4];
        
        int linha = 0;
        for (Venda venda : listaVenda) {
            
            obj[linha][0] = venda.getId();
            obj[linha][1] = venda.getCliente().getId();
            obj[linha][2] = venda.getCliente().getNome();
            String vlTotal = (venda.getVlTotal()) + "";
            if(vlTotal.contains(".")){
                vlTotal += "000";
                vlTotal = vlTotal.substring(0, vlTotal.indexOf(".") +3);
            }
            obj[linha][3] = vlTotal;
            linha++;
        }
        
        
        TableVenda.setModel(new javax.swing.table.DefaultTableModel(
            obj,
            new String [] {
                "Id Venda", "Id Cliente", "Nome Cliente", "Total Venda"
            }

        ));
        TableColumn colIdCompra = TableVenda.getColumnModel().getColumn(0);
        TableColumn colIdCliente = TableVenda.getColumnModel().getColumn(1);
        TableColumn colNomeCliente = TableVenda.getColumnModel().getColumn(2);
        TableColumn colTotalCompra = TableVenda.getColumnModel().getColumn(3);

        colIdCompra.setPreferredWidth(60);
        colIdCliente.setPreferredWidth(60);
        colNomeCliente.setPreferredWidth(255);
        colTotalCompra.setPreferredWidth(100);
        
    }//GEN-LAST:event_consultarVendaActionPerformed
