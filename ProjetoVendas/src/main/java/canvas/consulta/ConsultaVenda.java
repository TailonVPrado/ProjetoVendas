    private String montaOrderBy(){
        String retorno = "";
        
        int indiceSelecionado = ordemVenda.getSelectedIndex();
        switch (indiceSelecionado) {
            case 0:
                retorno = " order by cliente.nome ";
                break;
            case 1:
                retorno = " order by venda.id ";
                break;
            case 2:
                retorno = " order by venda.vl_total ";
                break;
        }
        
        if(tipoOrdencaoVenda.getSelectedIndex() == 0)
            retorno +=  " asc ";
        else if(tipoOrdencaoVenda.getSelectedIndex() == 1)
            retorno +=  " desc ";
        
        return retorno;
    }
    
    private String montaWhere(){
        String retorno = "";
        if(!(idClienteVenda.getText().equals(""))){
            retorno += " and cliente.id = " + idClienteVenda.getText();
        }
        if(!(cpfCliente.getText().equals(""))){
            retorno += "and cliente.cpf = '" + cpfCliente.getText() +"' ";
        }
        if(!(idVenda.getText().equals(""))){
            retorno += " and venda.id = " + idVenda.getText();
        }
        if(!(valorInicialVenda.getText().equals(""))){
            retorno += " and venda.vl_total > " + valorInicialVenda.getText();
        }
        if(!(valorFinalVEnda.getText().equals(""))){
            retorno += " and venda.vl_total < " + valorFinalVEnda.getText();
        }
        if(!(nomeClienteVenda.getText().equals(""))){
            retorno += " and upper(cliente.nome) like '%" + nomeClienteVenda.getText().toUpperCase() +"%'";
        }
        
        if(!(descricaoItemVenda.getText().equals(""))){
            retorno += " and exists (select 1 " +
                                      "from itemvenda, " +
                                           "item " +
                                     "where itemvenda.id_venda = venda.id " +
                                       "and itemvenda.id_item  = item.id " +
                                       "and upper(item.descricao) like ('%"+descricaoItemVenda.getText().toUpperCase()+"%'))";
        }
        if(!(idItemVenda.getText().equals(""))){
            retorno += " and exists (select 1 " +
                                      "from itemvenda, " +
                                           "item " +
                                     "where itemvenda.id_venda = venda.id " +
                                       "and itemvenda.id_item  = " + idItemVenda.getText() + " )";
        }
        return retorno;
    }
