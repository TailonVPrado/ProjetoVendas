    private void excluiItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluiItemActionPerformed
        Item item = new Item();
        item.setId(Integer.parseInt(idItem.getText()));
        item.setDescricao(descricaoItem.getText());
        item.setVlItem(Double.parseDouble(valorItem.getText()));
        ItemService itemService = new ItemService();
        
        try {
            Object[] options = { "Sim", "Não" }; 
            int opcao = JOptionPane.showOptionDialog(null, "Deseja realmente excluir este registro?", "Exclusão de Registro", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]); 
            if (opcao == 0) {
                itemService.delete(item);
                JOptionPane.showMessageDialog(null, "Item ("+item.getId()+") excluido com sucesso");
                idItem.setText("");
                descricaoItem.setText("");
                valorItem.setText("");
                excluiItem.setEnabled(false);
                atualizaItem.setText("Cadastrar");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao excluir Item. Erro: " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir Item. Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_excluiItemActionPerformed

